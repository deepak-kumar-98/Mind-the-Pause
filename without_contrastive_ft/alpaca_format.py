import json

# File paths
dis_file = "/home/paritosh/zero-shot-disfluency-detection/disfluency_signal_llama/data.dis"
label_file = "/home/paritosh/zero-shot-disfluency-detection/disfluency_signal_llama/data.label"
flu_file = "/home/paritosh/zero-shot-disfluency-detection/disfluency_signal_llama/data.flu"
output_file = "/home/paritosh/zero-shot-disfluency-detection/disfluency_signal_llama/alpaca_disfluency_dataset.jsonl"

# Load lines
with open(dis_file, 'r', encoding='utf-8') as f:
    dis_lines = [line.strip().split() for line in f]   # word-level tokens
with open(label_file, 'r', encoding='utf-8') as f:
    label_lines = [list(map(int, line.strip().split())) for line in f]
with open(flu_file, 'r', encoding='utf-8') as f:
    flu_lines = [line.strip() for line in f]

# instruction = (
#     "You are given a disfluent sentence generated by an Automatic Speech Recognition (ASR) system.\n"
#     "This sentence may contain disfluencies, including but not limited to repetitions, fillers, discourse markers, false starts, and other forms of disfluent speech in Hindi, Bengali and Marathi.\n\n"
#     "Your task is to remove these disfluencies while preserving the grammatical structure and original meaning of the sentence.\n\n"
