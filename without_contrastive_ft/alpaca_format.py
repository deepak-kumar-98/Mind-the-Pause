#     "Here are some examples of word-label pairs:\n"
#     "    Hindi:  [(\"यह\", 0), (\"किसानों\", 1), (\"के\", 1), (\"किसानों\", 0), (\"के\", 0), (\"पुरुषार्थ\", 0)]\n"
#     "    Bengali: [(\"হ্যাঁ\", 0), (\"আহ\", 1), (\",\", 0), (\"কি\", 0), (\"নিয়ে\", 0), (\"সাদা\", 0), (\"রঙের\", 0)]\n"
#     "    Marathi: [(\"आम्ही\", 0), (\"म्हणजे\", 1), (\"आम्ही\", 0), (\"शेतकरी\", 0), (\"काम\", 0), (\"करतो\", 0)]\n"
